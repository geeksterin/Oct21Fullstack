// "static void main" must be defined in a public class.
public class Main {
    public static void main(String[] args) {
        
        for (int i = 5; i >=1; i=i-1){
            
            for (int j = 1; j<=i; j=j+1){
                System.out.print("*");
            }
            
            System.out.println("");
        }
    }
}

