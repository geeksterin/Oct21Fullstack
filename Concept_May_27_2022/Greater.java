// "static void main" must be defined in a public class.
public class Greater {
    public static void main(String[] args) {
       // print the greater number between 500 and 1000;
        
        int num1 = 500;
        int num2 = 1000;
        
        if (num1 > num2){
            System.out.print(num1);
        }else{
            System.out.print(num2);
        }
    }
}