public class Main {
    public static void main(String[] args) {
        
        // equal to
        System.out.println(100 == 100);
        System.out.println(1000 == 10);
        System.out.println(1 == 500);
        
        System.out.println("-----");
         // not equal to
        System.out.println(100 != 100);
        System.out.println(1000 != 10);
        System.out.println(1 != 500);
        
        System.out.println("-----");
         // greater than
        System.out.println(100 > 100);
        System.out.println(1000 > 10);
        System.out.println(1 > 500);
        
        
         System.out.println("-----");
         // less than
        System.out.println(100 < 100);
        System.out.println(1000 < 10);
        System.out.println(1 < 500);
        
        
         System.out.println("-----");
         // greater than or equal to
        System.out.println(100 >= 100);//true
        System.out.println(1000 >= 10);//true
        System.out.println(1 >= 500); //false
        
        System.out.println("-----");
         // less than or equal to
        System.out.println(100 <= 100); //true
        System.out.println(1000 <= 10);//false
        System.out.println(1 <= 500);//true
        
    }
}
/*
relational operators

They tell the relation between two values/data

Greater than >
Less Than <
Equal to ==
Not equal to !=
Greater than or equal to   >=
Less than or equal to <=

The output of relational operators is always a boolean. True/false



*/